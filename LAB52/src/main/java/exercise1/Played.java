package exercise1;

public class Played {
    private int playedId;
    private int playerId;
    private int gameId;
    private double hoursPlayed;
    private int score;

    public int getPlayedId() { return playedId; }
    public void setPlayedId(int playedId) { this.playedId = playedId; }

    public int getPlayerId() { return playerId; }
    public void setPlayerId(int playerId) { this.playerId = playerId; }

    public int getGameId() { return gameId; }
    public void setGameId(int gameId) { this.gameId = gameId; }

    public double getHoursPlayed() { return hoursPlayed; }
    public void setHoursPlayed(double hoursPlayed) { this.hoursPlayed = hoursPlayed; }

    public int getScore() { return score; }
    public void setScore(int score) { this.score = score; }
}
